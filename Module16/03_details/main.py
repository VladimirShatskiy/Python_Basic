shop = [['каретка', 1200], ['шатун', 1000], ['седло', 300],
        ['педаль', 100], ['седло', 1500], ['рама', 12000],
        ['обод', 2000], ['шатун', 200], ['седло', 2700]]
price = 0
count_part = 0

part = input("Деталь для поиска:" )

for i_part in shop:
        if i_part[0] == part:
                count_part += 1
                price += i_part[1]

print(f"Кол-во деталей - {count_part}\n"
      f"Общая стоимость - {price}")